package basic;

public class VariableDemo3 {

  public static void main(String[] args) {
    int a = 2;
    int b = 4;

    int sum = a + b;
    System.out.println("sum=" + sum);

    int dis = a - b;
    System.out.println("dis=" + dis);

    int mul = a * b;
    System.out.println("mul=" + mul);

    System.out.println("div=" + (a / b));

  }

}
